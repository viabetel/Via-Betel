// Helper para obter todas as cidades de um estado
export function getCitiesByState(stateSigla: string): string[] {
  return CITIES_BY_STATE[stateSigla] || []
}

// Helper para obter o nome completo do estado pela sigla
export function getStateName(sigla: string): string {
  const state = BRAZIL_STATES.find((s) => s.sigla === sigla)
  return state?.nome || sigla
}

// Helper para obter todas as cidades (todas juntas)
export function getAllCities(): string[] {
  return Object.values(CITIES_BY_STATE).flat().sort()
}
